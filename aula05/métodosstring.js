let nome = 'Nicolas Fukunaga'
console.log('Nicolas Fukunaga'[0]); // Retorna a quantidade de caracteres presentes da string.

console.log(nome.charAt(8));  // charAt acessa  determinado caractér baseado no índice escolhido.

console.log(nome.toLocaleLowerCase()); // Transforma todos os caracteres em minúsculo. 

console.log(nome.toUpperCase()); // Transforma todos os caracteres em maiúsculo.

console.log(nome.substring(0, 8)); // Corta a string a partir do primeiro índice até o segundo índice.

console.log(nome.split('')); // Split separa a string de acordo com o que você especificou. 

